def can_form_word(word, elements):
    if word == "":
        return ""
    word_lower = word.lower()
    for element in elements:
        element_lower = element.lower()
        if word_lower.startswith(element_lower):
            remaining_word = word_lower[len(element_lower):]
            result = can_form_word(remaining_word, elements)
            if result is not None:
                return element + result
    return None

